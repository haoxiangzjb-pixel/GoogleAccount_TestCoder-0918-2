function m = mean_func(data)
% MEAN_FUNC Вычисляет среднее значение элементов массива.
%   m = mean_func(data) возвращает среднее арифметическое всех элементов
%   входного массива data.
%
%   Входные аргументы:
%       data - числовой вектор или матрица
%
%   Выходные аргументы:
%       m - среднее значение
%
%   Пример:
%       mean_func([1, 2, 3, 4]) returns 2.5

    % Проверка на пустой ввод
    if isempty(data)
        error('Входной массив не может быть пустым.');
    end
    
    % Проверка на числовой тип данных
    if ~isnumeric(data)
        error('Входные данные должны быть числовыми.');
    end
    
    % Вычисление среднего значения
    m = sum(data(:)) / numel(data);
end
