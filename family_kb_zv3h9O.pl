% База фактов о семье

% Предикаты: родитель(Родитель, Ребенок)
родитель(иван, мария).
родитель(иван, анна).
родитель(мария, дмитрий).
родитель(мария, елена).
родитель(анна, алексей).

% Предикаты: пол(Имя, Пол)
пол(иван, мужчина).
пол(мария, женщина).
пол(анна, женщина).
пол(дмитрий, мужчина).
пол(елена, женщина).
пол(алексей, мужчина).

% Правило: бабушка(Бабушка, Внук)
бабушка(X, Z) :- родитель(X, Y), родитель(Y, Z), пол(X, женщина).

% Правило: дедушка(Дедушка, Внук)
дедушка(X, Z) :- родитель(X, Y), родитель(Y, Z), пол(X, мужчина).

% Правило: внук(Внук, Бабушка_или_Дедушка)
внук(Z, X) :- (бабушка(X, Z); дедушка(X, Z)).