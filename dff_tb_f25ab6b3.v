// Тестовая плата для D-триггера
`timescale 1ns/1ps

module dff_testbench;
    reg clk;
    reg reset;
    reg d;
    wire q, q_bar;

    // Создание экземпляра тестируемого модуля (предполагаем, что он называется d_flipflop)
    d_flipflop uut (
        .clk(clk),
        .reset(reset),
        .d(d),
        .q(q),
        .q_bar(q_bar)
    );

    // Генерация тактового сигнала (10 нс период)
    initial begin
        clk = 0;
        forever #5 clk = ~clk;  // Переключение каждые 5 нс, период 10 нс
    end

    // Тестовая последовательность
    initial begin
        // Инициализация сигналов
        reset = 1;
        d = 0;
        
        // Сброс
        #15;
        reset = 0;
        
        // Тестирование различных входных комбинаций
        #10 d = 1;     // Установка D=1 на следующий такт
        #20 d = 0;     // Установка D=0 на следующий такт
        #20 d = 1;     // Установка D=1 на следующий такт
        #20 d = 0;     // Установка D=0 на следующий такт
        #20 d = 1;     // Установка D=1 на следующий такт
        
        // Тестирование сброса
        #10 reset = 1;
        #15 reset = 0;
        
        // Дополнительные тесты
        #20 d = 1;
        #20 d = 0;
        
        #50 $finish;   // Завершение симуляции
    end

    // Отслеживание сигналов
    initial begin
        $monitor("Time=%0t ns, CLK=%b, RESET=%b, D=%b, Q=%b, Q_bar=%b", 
                 $time, clk, reset, d, q, q_bar);
    end

endmodule