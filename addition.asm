section .text
    global _start

; Функция сложения двух 32-битных целых чисел
; Вход: EAX = первое число, EBX = второе число
; Выход: EAX = результат сложения
add_func:
    add eax, ebx        ; Складываем EBX с EAX, результат в EAX
    ret                 ; Возвращаемся из функции

_start:
    ; Пример использования функции
    mov eax, 53         ; Загружаем первое число в EAX
    mov ebx, 27         ; Загружаем второе число в EBX
    call add_func       ; Вызываем функцию сложения

    ; Результат теперь в EAX (53 + 27 = 80)

    ; Завершаем программу
    mov eax, 1          ; Системный вызов sys_exit
    int 0x80            ; Вызов ядра