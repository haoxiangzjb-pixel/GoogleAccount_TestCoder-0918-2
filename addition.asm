; Функция сложения двух 32-битных целых чисел на x86 (NASM синтаксис)
; Принимает два аргумента через стек: arg1 и arg2
; Возвращает результат сложения в EAX

section .text
    global add_function

add_function:
    ; Сохраняем регистры, которые будем использовать
    push ebp
    mov ebp, esp

    ; Загружаем аргументы из стека
    ; [ebp + 8]  - первый аргумент (arg1)
    ; [ebp + 12] - второй аргумент (arg2)
    mov eax, [ebp + 8]   ; Загружаем arg1 в EAX
    add eax, [ebp + 12]  ; Прибавляем arg2 к EAX

    ; Восстанавливаем регистры
    mov esp, ebp
    pop ebp
    ret                  ; Результат находится в EAX