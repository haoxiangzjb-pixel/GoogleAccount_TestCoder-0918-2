% База фактов о семье

% Предикаты: родитель(Родитель, Ребенок)
родитель(иван, мария).
родитель(иван, анна).
родитель(мария, дмитрий).
родитель(мария, елена).
родитель(анна, алексей).

% Предикаты: пол(Имя, Пол)
пол(иван, мужской).
пол(мария, женский).
пол(анна, женский).
пол(дмитрий, мужской).
пол(елена, женский).
пол(алексей, мужской).

% Правило: бабушка(Бабушка, Внук)
бабушка(X, Z) :- родитель(X, Y), родитель(Y, Z), пол(X, женский).

% Правило: общий_родитель(X, Y) - X и Y имеют одного и того же родителя
общий_родитель(X, Y) :- родитель(Z, X), родитель(Z, Y), X \= Y.