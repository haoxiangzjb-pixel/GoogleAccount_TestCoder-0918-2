% База фактов Prolog о семье

% Семейные отношения
родитель(мария, иван).
родитель(мария, анастасия).
родитель(пётр, иван).
родитель(пётр, анастасия).
родитель(иван, дмитрий).
родитель(иван, елена).
родитель(светлана, дмитрий).
родитель(светлана, елена).
родитель(анастасия, алексей).
родитель(андрей, алексей).

% Пол
мужчина(пётр).
мужчина(иван).
мужчина(дмитрий).
мужчина(андрей).
мужчина(алексей).

женщина(мария).
женщина(анастасия).
женщина(светлана).
женщина(елена).

% Брак
супруги(мария, пётр).
супруги(иван, светлана).
супруги(анастасия, андрей).

% Правила
отец(X, Y) :- родитель(X, Y), мужчина(X).
мать(X, Y) :- родитель(X, Y), женщина(X).
брат(X, Y) :- мужчина(X), родитель(Z, X), родитель(Z, Y), X \= Y.
сестра(X, Y) :- женщина(X), родитель(Z, X), родитель(Z, Y), X \= Y.
дедушка(X, Y) :- родитель(X, Z), родитель(Z, Y), мужчина(X).
бабушка(X, Y) :- родитель(X, Z), родитель(Z, Y), женщина(X).