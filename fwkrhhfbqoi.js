/**
 * Function that returns the sum of two numbers
 * @param {number} a - First number
 * @param {number} b - Second number
 * @returns {number} The sum of a and b
 */
function sum(a, b) {
    return a + b;
}

// Example usage:
// console.log(sum(2, 3)); // Output: 5
// console.log(sum(-1, 5)); // Output: 4

module.exports = sum;