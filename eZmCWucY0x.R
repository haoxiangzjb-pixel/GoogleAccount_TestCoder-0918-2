# R-скрипт для построения диаграммы рассеяния

# Создание случайных данных для примера
set.seed(42)  # Для воспроизводимости
x <- rnorm(100)
y <- 2 * x + rnorm(100, sd = 0.5)  # y зависит от x с некоторым шумом

# Создание диаграммы рассеяния
plot(x, y, 
     main = "Диаграмма рассеяния", 
     xlab = "X переменная", 
     ylab = "Y переменная",
     pch = 19,  # Сплошные точки
     col = "blue"  # Цвет точек
)

# Добавление линии тренда
abline(lm(y ~ x), col = "red", lwd = 2)

# Сохранение графика в файл (в формате PNG)
png(filename = "scatter_plot.png", width = 800, height = 600)
plot(x, y, 
     main = "Диаграмма рассеяния", 
     xlab = "X переменная", 
     ylab = "Y переменная",
     pch = 19, 
     col = "blue"
)
abline(lm(y ~ x), col = "red", lwd = 2)
dev.off()

cat("Диаграмма рассеяния создана и сохранена в файл scatter_plot.png\n")