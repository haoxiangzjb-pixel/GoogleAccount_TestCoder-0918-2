// Тестовая плата для D-триггера
`timescale 1ns / 1ps

module tb_d_flip_flop;
    reg clk;
    reg rst;
    reg d;
    wire q;

    // Подключение тестируемого модуля (предполагается, что он называется d_flip_flop)
    d_flip_flop uut (
        .CLK(clk),
        .RST(rst),
        .D(d),
        .Q(q)
    );

    // Генерация тактового сигнала
    initial begin
        clk = 0;
        forever #5 clk = ~clk; // Тактовый период 10 (5+5)
    end

    // Сценарий тестирования
    initial begin
        // Инициализация
        rst = 1;
        d = 0;
        #10;

        // Сброс
        rst = 1;
        #10;

        // Снятие сброса
        rst = 0;
        #10;

        // Тест 1: D = 1 нарастающий фронт
        d = 1;
        #10; // Ждем следующего фронта
        #5;  // Проверка после половины периода

        // Тест 2: D = 0 нарастающий фронт
        d = 0;
        #10; // Ждем следующего фронта
        #5;  // Проверка после половины периода

        // Тест 3: D = 1, сброс во время работы
        d = 1;
        #10;
        rst = 1; // Сброс
        #10;
        rst = 0; // Снятие сброса
        #20; // Дополнительное время для наблюдения

        $finish;
    end

endmodule