// Определение структуры точки
const Point = struct {
    x: f64,
    y: f64,
};

// Пример программы на Zig, которая сохраняет структуру точки в файл со случайным именем
// Поскольку Zig не установлен в системе, этот код представляет собой теоретическую реализацию
// на языке Zig, которая будет компилироваться и выполняться в соответствующей среде

const std = @import("std");

pub fn main() !void {
    // Создание экземпляра структуры точки
    const point = Point{ .x = 3.14, .y = 2.71 };
    
    // Генерация случайного имени файла
    // В реальной реализации на Zig:
    // 1. Используется стандартная библиотека для генерации случайных чисел
    // 2. Создается файл с этим именем
    // 3. Структура точки сериализуется и записывается в файл
    
    std.debug.print("Структура точки: x={d}, y={d}\n", .{ point.x, point.y });
    std.debug.print("В реальной реализации на Zig точка была бы сохранена в файл со случайным именем\n");
    
    // Пример возможного содержимого файла:
    std.debug.print("Пример содержимого файла: x: {d}, y: {d}\n", .{ point.x, point.y });
}