# Загрузка необходимых библиотек
# Базовая функция plot() не требует дополнительных пакетов

# Создание случайных данных
set.seed(42) # Для воспроизводимости
x <- rnorm(100) # 100 случайных значений из нормального распределения
y <- 2 * x + rnorm(100, sd = 0.5) # Зависимость y от x с небольшим шумом

# Создание имени файла
# Используем случайное число и текущую дату для генерации уникального имени
random_number <- sample(10000:99999, 1)
plot_filename <- paste0("scatter_plot_", Sys.Date(), "_", random_number, ".png")

# Сохранение диаграммы рассеяния в файл
png(filename = plot_filename, width = 800, height = 600) # Указываем размеры изображения
plot(x, y,
     main = "Scatter Plot of Random Data", # Заголовок
     xlab = "X Variable", # Подпись оси X
     ylab = "Y Variable", # Подпись оси Y
     pch = 19, # Стиль точек
     col = "blue" # Цвет точек
)
abline(lm(y ~ x), col = "red") # Добавляем линию линейной регрессии
dev.off() # Закрываем графическое устройство

cat("График сохранен в файл:", plot_filename, "\n")