section .text
global _start

; Функция сложения двух 32-битных целых чисел
; Вход: EAX = первое число, EBX = второе число
; Выход: EAX = результат сложения
add_func:
    add eax, ebx    ; Складываем EBX с EAX, результат в EAX
    ret             ; Возвращаемся из функции

; Точка входа в программу
_start:
    ; Загружаем числа для сложения в EAX и EBX
    mov eax, 55     ; Пример: первое число
    mov ebx, 45     ; Пример: второе число

    ; Вызываем функцию сложения
    call add_func

    ; Результат теперь в EAX (в данном случае, 100)

    ; Завершаем программу (системный вызов exit)
    mov ebx, 0      ; Код возврата
    mov eax, 1      ; Номер системного вызова (sys_exit)
    int 0x80        ; Вызов ядра Linux