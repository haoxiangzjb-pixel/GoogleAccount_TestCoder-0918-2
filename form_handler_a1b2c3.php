<?php
// Простой обработчик формы
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    // Пример получения данных из формы
    $user_input = $_POST['user_input'] ?? '';

    // Простая валидация (в реальном приложении потребуется более строгая валидация и экранирование)
    if (!empty($user_input)) {
        echo "Вы ввели: " . htmlspecialchars($user_input);
    } else {
        echo "Ошибка: Поле ввода пусто.";
    }
} else {
    echo "Метод запроса не поддерживается.";
}
?>