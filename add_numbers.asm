section .text
    global _start

; Функция для сложения двух чисел
; Вход: EAX = первое число, EBX = второе число
; Выход: EAX = результат сложения
add_func:
    add eax, ebx
    ret

_start:
    ; Загружаем числа в регистры
    mov eax, 5      ; Первое число
    mov ebx, 10     ; Второе число

    ; Вызываем функцию сложения
    call add_func

    ; Результат теперь в EAX (ожидаем 15)

    ; Выход из программы
    mov ebx, 0      ; Код возврата
    mov eax, 1      ; Системный вызов exit
    int 0x80