# Метод для вычисления факториала числа
def factorial(n)
  # Проверка на неотрицательное целое число
  raise ArgumentError, "Факториал определен только для неотрицательных целых чисел" unless n.is_a?(Integer) && n >= 0

  # Факториал 0 равен 1 по определению
  return 1 if n == 0

  # Вычисление факториала с помощью reduce
  (1..n).reduce(:*)
end

# Пример использования:
# puts factorial(5) # => 120