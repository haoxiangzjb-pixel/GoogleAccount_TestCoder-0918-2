% База фактов о семье

% Предикаты: родитель(Родитель, Ребенок)
родитель(иван, мария).
родитель(иван, алексей).
родитель(мария, дмитрий).
родитель(мария, елена).
родитель(алексей, василий).
родитель(алексей, анастасия).

% Предикаты: пол(Имя, Пол)
пол(иван, мужчина).
пол(мария, женщина).
пол(алексей, мужчина).
пол(дмитрий, мужчина).
пол(елена, женщина).
пол(василий, мужчина).
пол(анастасия, женщина).

% Правила:

% брат(X, Y) - X является братом Y
брат(X, Y) :-
    родитель(Z, X),
    родитель(Z, Y),
    X \= Y,
    пол(X, мужчина).

% сестра(X, Y) - X является сестрой Y
сестра(X, Y) :-
    родитель(Z, X),
    родитель(Z, Y),
    X \= Y,
    пол(X, женщина).

% дедушка(X, Y) - X является дедушкой Y
дедушка(X, Y) :-
    родитель(X, Z),
    родитель(Z, Y),
    пол(X, мужчина).

% бабушка(X, Y) - X является бабушкой Y
бабушка(X, Y) :-
    родитель(X, Z),
    родитель(Z, Y),
    пол(X, женщина).

% внук(X, Y) - X является внуком Y
внук(X, Y) :-
    родитель(Y, Z),
    родитель(Z, X),
    пол(X, мужчина).

% внучка(X, Y) - X является внучкой Y
внучка(X, Y) :-
    родитель(Y, Z),
    родитель(Z, X),
    пол(X, женщина).
