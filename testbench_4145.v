// Тестовая плата для D-триггера
module testbench;
  reg clk;
  reg reset;
  reg d;
  wire q, q_not;

  // Инстанс тестируемого модуля (предполагается, что модуль d_flip_flop уже существует)
  d_flip_flop uut (
    .clk(clk),
    .reset(reset),
    .d(d),
    .q(q),
    .q_not(q_not)
  );

  // Генерация тактового сигнала (например, период 10)
  initial begin
    clk = 0;
    forever #5 clk = ~clk; // Переключение каждые 5 единиц времени, период = 10
  end

  // Инициализация сигнала сброса и тестирование
  initial begin
    reset = 1; // Установка сброса
    d = 0;
    #15; // Ждем 15 единиц времени
    reset = 0; // Сброс завершен

    // Проверка различных состояний входа D
    d = 1; #20; // Установка D в 1, ожидание 20
    d = 0; #20; // Установка D в 0, ожидание 20
    d = 1; #20; // Установка D в 1, ожидание 20
    d = 0; #20; // Установка D в 0, ожидание 20

    $finish; // Завершение симуляции
  end

endmodule