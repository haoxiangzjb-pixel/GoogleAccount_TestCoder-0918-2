/**
 * Интерфейс, описывающий сущность Продукт.
 */
interface Product {
  /**
   * Уникальный идентификатор продукта.
   */
  id: number;

  /**
   * Название продукта.
   */
  название: string;

  /**
   * Цена продукта.
   */
  цена: number;
}

// Пример использования
const sampleProduct: Product = {
  id: 1,
  название: "Ноутбук",
  цена: 999.99
};

console.log(sampleProduct);