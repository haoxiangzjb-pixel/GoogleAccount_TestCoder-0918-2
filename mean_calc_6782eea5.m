function [avg] = mean_calc(data)
% MEAN_CALC Вычисляет среднее значение массива
%   avg = mean_calc(data) возвращает среднее арифметическое
%   элементов входного массива data.
%
%   Входные параметры:
%       data - числовой вектор или матрица
%
%   Выходные параметры:
%       avg - среднее значение всех элементов

    % Проверка на пустой ввод
    if isempty(data)
        error('Входной массив не может быть пустым.');
    end
    
    % Проверка на числовой тип
    if ~isnumeric(data)
        error('Входные данные должны быть числовыми.');
    end
    
    % Вычисление среднего значения
    avg = sum(data(:)) / numel(data);

end
